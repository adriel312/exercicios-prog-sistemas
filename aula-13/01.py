#Crie um programa que exiba uma contagem regressiva de 10 a 1 e, em seguida, exiba "Feliz Ano Novo!".
contador = 10   #contador inicia em 10, primeiro nÃºmero da sequencia
while contador >=1:
    print(contador)
    contador-=1     #o contador deve decrementar para realizar uma contagem regressiva
print("Feliz Ano Novo !!!")